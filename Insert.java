    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        if (validateFields()) {
            try {
                connection();
                String query = "INSERT INTO RESERVATION (RNum, RTime, NumOfPeople, `Table`, CRPhone) VALUES (?, ?, ?, ?, ?)";
                try (PreparedStatement stat = con.prepareStatement(query)) {
                    stat.setString(1, jTextField1.getText());
                    stat.setString(2, jTextField2.getText());
                    stat.setInt(3, Integer.parseInt(jTextField3.getText()));
                    stat.setInt(4, Integer.parseInt(jTextField4.getText()));
                    stat.setString(5, jTextField5.getText());

                    stat.executeUpdate();
                    javax.swing.JOptionPane.showMessageDialog(this, "RESERVATION inserted successfully!");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            } finally {
                try {
                    if (con != null) con.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

