    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        String sql = "INSERT INTO `ORDER` (OrderTime, OrderNumber, ORNum, BNationalAddress) VALUES (?, ?, ?, ?)";

        // Input validation
        if (jTextField8.getText().isEmpty() || jTextField7.getText().isEmpty() ||
            jTextField10.getText().isEmpty() || jTextField9.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields before submitting.");
            return;
        }

       try{
            connection();
            PreparedStatement pstmt = con.prepareStatement(sql);

            // Set parameters
            pstmt.setString(1, jTextField7.getText());
            pstmt.setString(2, jTextField8.getText());
            pstmt.setString(3, jTextField10.getText());
            pstmt.setString(4, jTextField9.getText());

            // Execute the statement
            int rowsInserted = pstmt.executeUpdate();
            if (rowsInserted > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Order inserted successfully!");
            }

        } catch (SQLException | ClassNotFoundException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error",  javax.swing.JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); // For debugging, remove in production
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid number format in Order Number or ORNum.", "Input Error",  javax.swing.JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton6ActionPerformed

