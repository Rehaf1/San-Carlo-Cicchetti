private boolean validateFields() {
    if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || 
        jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() || 
        jTextField5.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields.", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        return false;
    }
    try {
        Integer.parseInt(jTextField3.getText());
        Integer.parseInt(jTextField4.getText());
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter valid numbers for Number of People and Table.", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}

